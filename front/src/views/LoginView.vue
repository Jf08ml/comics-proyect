<template>
  <div class="container">
    <div class="content">
      <div class="content-title">
        <h1>Log in</h1>
      </div>
      <div class="content-form">
        <form @submit.prevent="onSubmit">
          <input v-model="username" placeholder="Email" type="text" required>
          <p v-if="username == ''" style="color: red;">Este campo es requerido</p>

          <input v-model="password" placeholder="Password" type="password" required>
          <p v-if="password == ''" style="color: red;">Este campo es requerido</p>
          <router-link to="/recuperar">
            <p>¿Olvidaste tu contraseña?</p>
          </router-link>



          <button type="submit" class="button-primary">Login</button>

        </form>
      </div>
    </div>
  </div>
</template>
  
<script setup>
import { ref } from 'vue'
import { useRouter } from 'vue-router'
import '@/assets/scss/login/login.scss';
import { useAuthStore } from '@/store/auth';

const router = useRouter();
const authStore = useAuthStore();

const username = ref('juanf@example.com');
const password = ref('contraseña123');

const onSubmit = async () => {
  try {
    await authStore.login(username.value, password.value);
    router.push("/realcomics");
  } catch (error) {
    console.error(error)
  }
};

</script>
  